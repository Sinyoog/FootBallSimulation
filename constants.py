    lo_o, lo_s = a["floor_ovr"], a["floor_sal"]
    hi_o, hi_s = a["ceil_ovr"], a["ceil_sal"]
    # floor_ovr 미만은 0으로 클램프(지수보간 특성상 아래로 급격히 0에 수렴하는 것 방지).
    # ceil_ovr 초과는 그대로 외삽(월드클래스를 넘는 극소수 최상급 이적생 반영).
    frac = max(0.0, (ovr - lo_o) / (hi_o - lo_o))
    val = lo_s * (hi_s / lo_s) ** frac
    return max(0, int(val))


